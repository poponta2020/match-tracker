package com.karuta.matchtracker.util;

/**
 * 百人一首 決まり字マスター（100枚・確定版）のサーバー複製。
 *
 * <p>フロントの {@code karuta-tracker-ui/src/data/kimariji.js} の {@code KIMARIJI} を
 * <b>補正値込み</b>で複製したもの。札分けリマインダーのテキスト生成（抜き札の {@code 番号(決まり字)抜き}）で
 * サーバー送信するために BE に持つ。FE/BE の一元化はせず、値の一致は
 * {@code CardDivisionTextService} のテストで担保する（Non-goal: 一元化）。
 *
 * <p>参照元の誤りは補正済み: 041=こひ、068=こころに、082=おも（kimariji.js と同一）。
 */
public final class Kimariji {

    private Kimariji() {}

    /** 札番号(1〜100) → 決まり字。index = cardNo - 1。 */
    private static final String[] KIMARIJI = {
        "あきの", "はるす", "あし", "たご", "おく", "かさ", "あまの", "わがい", "はなの", "これ",         // 1-10
        "わた・や", "あまつ", "つく", "みち", "きみ・は", "たち", "ちは", "す", "なにわが", "わび",         // 11-20
        "いまこ", "ふ", "つき", "この", "なにし", "おぐ", "みかの", "やまざ", "こころあ", "ありあ",         // 21-30
        "あさぼあ", "やまが", "ひさ", "たれ", "ひとは", "なつ", "しら", "わすら", "あさじ", "しの",         // 31-40
        "こひ", "ちぎりき", "あい", "おおこ", "あわれ", "ゆら", "やえ", "かぜを", "みかき", "きみ・お",     // 41-50
        "かく", "あけ", "なげき", "わすれ", "たき", "あらざ", "め", "ありま", "やす", "おおえ",             // 51-60
        "いに", "よを", "いまは", "あさぼう", "うら", "もろ", "はるの", "こころに", "あらし", "さ",         // 61-70
        "ゆう", "おと", "たか", "うか", "ちぎりお", "わた・こ", "せ", "あわじ", "あきか", "ながか",         // 71-80
        "ほ", "おも", "よの・よ", "ながら", "よも", "なげけ", "む", "なにわえ", "たま", "みせ",             // 81-90
        "きり", "わがそ", "よの・は", "みよ", "おおけ", "はなさ", "こぬ", "かぜそ", "ひとも", "もも",       // 91-100
    };

    /**
     * 札番号(1〜100) → 決まり字。範囲外は番号文字列でフォールバック（kimariji.js の挙動に合わせる）。
     */
    public static String of(int cardNo) {
        if (cardNo >= 1 && cardNo <= 100) {
            return KIMARIJI[cardNo - 1];
        }
        return String.valueOf(cardNo);
    }
}
